import '../../domain/entities/article.dart';

// we use model kyu k model bad me chane hosakta ha
// or entity ko change nhi karna chahye

class articleModel extends ArticleEntity {
  const articleModel({
    String? author,
    String? title,
    String? description,
    String? url,
    String? urlToImage,
    String? publishedAt,
    String? content,
  }) : super(
          author: author,
          title: title,
          description: description,
          url: url,
          urlToImage: urlToImage,
          publishedAt: publishedAt,
          content: content,
        );

  factory articleModel.fromJson(Map<String, dynamic> json) {
    return articleModel(
      author: json['author'],
      title: json['title'],
      description: json['description'],
      url: json['url'],
      urlToImage: json['urlToImage'],
      publishedAt: json['publishedAt'],
      content: json['content'],
    );
  }
}
